    Write-host "              " -BackgroundColor red
    Write-host "░░░░░░░░░░" -BackgroundColor Blue -NoNewline
    Write-host "              " -BackgroundColor White
    Write-host "░░░░░░░░░░" -BackgroundColor Blue -NoNewline
    Write-host "              " -BackgroundColor red
    Write-host "                        " -BackgroundColor White
    Write-host "                        " -BackgroundColor red
    Write-host "                        " -BackgroundColor White
    Write-host "                        `n" -BackgroundColor red
}
